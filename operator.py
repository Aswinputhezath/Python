value1 = int(input ('\n Enter the value1:'))
value2 = int(input ('\n Enter the value2:'))
print("TYPE OF VALUE1:",type(value1))
print("TYPE OF VALUE2:",type(value2))
add = value1 + value2
print ("ADD=",add)# adds the given value 
sub = value1 - value2
print ("SUB=",sub)# subtracts right operand from left
mul = value1 * value2
print ("MULTIPLY=",mul)# Multiplies the 2 operands 
div = value1 / value2  
print ("DIVISION_QUO_NOROUNDOFF=",div) # Divide left operand by the right one (always results into float)
mod = value1 % value2 
print ("MODULUS_REMAINDER=",mod)#Modulus - remainder of the division of left operand by the right
quo = value1 // value2
print ("QUOTIENT_DIV_ROUNDOFFVALUE_INT=",quo)#Floor division - division that results into whole number adjusted to the left in the number line
power = value1 ** value2
print ("POWER=",power)#a to the power b 



